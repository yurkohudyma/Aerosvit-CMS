package ua.hudyma.domain.visa;

public enum EntriesNumber {

    /**
     * Один в’їзд. Після виїзду віза стає недійсною.
     */
    SINGLE,

    /**
     * Два в’їзди. Наприклад: в'їзд → виїзд → ще один в’їзд.
     */
    DOUBLE,

    /**
     * Три в’їзди. Рідкісний варіант, зустрічається в окремих країнах (наприклад, Китай).
     */
    TRIPLE,

    /**
     * Багаторазова віза. Необмежена кількість в’їздів до закінчення терміну дії.
     */
    MULTIPLE,

    /**
     * Необмежена кількість в’їздів, можливо безтерміново або в межах особливого дипломатичного статусу.
     * Може бути тотожне до MULTIPLE, але іноді вживається окремо в системах.
     */
    UNLIMITED,

    /**
     * Один в’їзд з можливістю повернення у випадку надзвичайних обставин.
     * Дуже рідко використовується, зазвичай у спецрежимах.
     */
    SINGLE_PLUS
}
